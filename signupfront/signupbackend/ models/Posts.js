const mongoose = require('mongoose');
const makePostTemplate = new mongoose.Schema({
    username: {
        type: String,
        required: true
    },
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    body: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    },





})
module.exports = mongoose.model('posts', makePostTemplate)